class Note:
    """
    定义音乐中的一个音符。
    """

    def __init__(
        self,
        id: int,
        level: int = 0,
        length: float = 1.0,
        half_high: bool = False,
        half_low: bool = False
    ):
        """
        构造一个音符。
        """

        # 音符的标号，应为0~7之间的整数。
        # 其中0表示休止符。
        self.id: int = id

        # 音符的高低，应为-2~2之间的整数。
        # 为负数表示低音（-1为低音，-2为重低音），
        # 为正数表示高音（1为高音，2为重高音），
        # 为0表示正常音（不升音也不降音）。
        # 默认值：0
        self.level: int = level

        # 音符的时长，应为正浮点数。
        # 如：2.0表示二分音符，4.0表示全音符，
        # 1.0表示四分音符，0.5表示八分音符，
        # 0.25表示十六分音符，3.0表示二分附点音符，
        # 1.5表示四分附点音符，依次类推。
        # 默认值：1.0
        self.length: float = length

        # 音符是否升半音。
        # 默认值：False
        self.half_high: bool = half_high
        
        # 音符是否降半音。
        # 默认值：False
        self.half_low: bool = half_low


