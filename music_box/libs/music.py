def base_freqs(base: str) -> list[int]:
    """
    根据基大调返回对应的频率列表。
    """

    key = base
    match base:
        case 'Bb':
            # 降B大调即为升A大调
            key = 'A#'
        case 'Db':
            # 降D大调即为升C大调
            key = 'C#'
        case 'Eb':
            # 降E大调即为升D大调
            key = 'D#'
        case 'Gb':
            # 降G大调即为升F大调
            key = 'F#'
        case 'Ab':
            # 降A大调即为升G大调
            key = 'G#'
    
    return BASE_FREQS[key]